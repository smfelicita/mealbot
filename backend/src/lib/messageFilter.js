/**
 * Проверяет, соответствует ли сообщение контексту приложения (еда, кулинария, здоровье).
 * Работает без AI — только на основе ключевых слов.
 */

// Темы, которые явно не относятся к еде и кулинарии
const OFF_TOPIC_PATTERNS = [
  // Политика
  /\b(полити|президент|депутат|выбор[ыа]|парламент|санкц|война|нато|мвд|фсб|кремл|оппозиц|протест|митинг|конституц)\b/i,
  // Погода
  /\b(погода|температура воздух|прогноз погод|осадк|климат|ветер|дождь|снег|мороз)\b/i,
  // Финансы / крипта
  /\b(биткоин|криптовалют|акци[ия]|биржа|инвестиц|форекс|трейдинг|кредит|ипотек|банк[ро]|дефолт)\b/i,
  // Спорт (не связанный с питанием спортсменов)
  /\b(футбол|хоккей|баскетбол|теннис|формул[аы] 1|чемпионат|матч|турнир|тренировк[аи] спорт)\b/i,
  // Новости / медиа
  /\b(новост[иь]|сми|телевидени|журналист|репортаж|пропаганд)\b/i,
  // Развлечения (кино, музыка — не еда)
  /\b(фильм|кино|сериал|актёр|режиссёр|музыкант|певец|альбом|концерт|аниме)\b/i,
  // IT (не связанный с приложением)
  /\b(программирован|напиши код|python|javascript|sql запрос|хакинг|взлом сайт)\b/i,
  // Медицина (кроме питания)
  /\b(диагноз|таблетк|лечени[ея]|болезн[ьи]|операц|антибиотик|вирус гриппа|рак|онколог)\b/i,
  // История / география (не кухня)
  /\b(история росси|вторая мировая|холодная война|столиц[аы] страны|население страны)\b/i,
  // Юридические
  /\b(юрист|адвокат|суд|иск|договор аренд|развод|алимент)\b/i,
]

// Слова, которые делают сообщение точно релевантным — даже если есть спорные совпадения
const FOOD_SAFE_PATTERNS = [
  /\b(рецепт|блюд[оа]|ингредиент|приготов|готовить|готовлю|еда|питани|кулинар|продукт|закуск|салат|суп|десерт|завтрак|обед|ужин|перекус)\b/i,
  /\b(белк[ии]|жир[ыа]|углевод|калори|кбжу|витамин|диет[аы]|вегетари|веган|правильное питание|пп рецепт)\b/i,
  /\b(холодильник|список покупок|что приготовить|что поесть|что съесть|замена|заменить|из чего)\b/i,
]

/**
 * @param {string} message
 * @returns {{ allowed: boolean, reason?: string }}
 */
function checkMessageRelevance(message) {
  const text = message.trim()

  // Очень короткие сообщения пропускаем (продолжение диалога: "да", "нет", "спасибо")
  if (text.length < 15) return { allowed: true }

  // Если явно про еду — сразу разрешаем
  const isFoodRelated = FOOD_SAFE_PATTERNS.some(p => p.test(text))
  if (isFoodRelated) return { allowed: true }

  // Проверяем нерелевантные темы
  const offTopicMatch = OFF_TOPIC_PATTERNS.find(p => p.test(text))
  if (offTopicMatch) {
    return {
      allowed: false,
      reason: 'Я специализируюсь только на кулинарии, рецептах и питании. Задайте вопрос, связанный с едой.',
    }
  }

  // Нейтральные запросы — пропускаем (вопрос в контексте диалога)
  return { allowed: true }
}

module.exports = { checkMessageRelevance }
